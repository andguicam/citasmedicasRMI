    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        // eliminar
        String id_eliminar = jTextField2.getText();
        List<Cita> citas = null;
        boolean aux = false;
        try {
            citas = medico.verCitas();
            
            for (Cita c : citas) {
                if (c.getId().equals(id_eliminar) && c.getMedicoResponsable().getDni().equals(medico.getDni())) {
                    medico.eliminarCita(c);
                    aux = true;
                    break;
                }
            }
        } catch (RemoteException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
        if (aux) {
                jTextField1.setText("Eliminado");
            }else{
                jTextField1.setText("Error Eliminar");
            }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        // TODO add your handling code here:
        List<Cita> citas = null;
        boolean aux = false;
        try {
            citas = medico.verCitas();
            aux = true;
        } catch (RemoteException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }

        String texto = "";

        for (Cita c : citas) {
            texto += c + "\n";
        }

        jTextArea1.setText(texto);
        if (aux) {
                jTextField1.setText("Correcto");
            }else{
                jTextField1.setText("Error ");
            }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
       String id_agregar = jTextField3.getText();
        String consulta_agregar = jTextField6.getText();
        boolean aux = false;

        try {
            Date sf_ini = new SimpleDateFormat("yyyy-MM-DD HH:mm").parse(jTextField5.getText());
            Date sf_fin = new SimpleDateFormat("yyyy-MM-DD HH:mm").parse(jTextField4.getText());
            medico.agregarCita(id_agregar, sf_ini, sf_fin, consulta_agregar, medico);
            aux = true;
        } catch (ParseException e1) {
            System.out.println("Se ha introducido la fecha en un formato incorrecto.");
            System.out.print("Formato esperado: YYYY-MM-DD HH:mm");
            e1.printStackTrace();
        } catch (RemoteException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        } 
        if (aux) {
                jTextField1.setText("Agregado");
            }else{
                jTextField1.setText("Error Agregar");
            }
    }//GEN-LAST:event_jButton5ActionPerformed

